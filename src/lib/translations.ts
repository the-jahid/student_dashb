  export const getTranslation = (key: string, language: string): string => {
    // Default to English if language not found
    const languageTranslations = translations[language as keyof typeof translations] || translations.en
  
    // Return the translation or the key itself if not found
    return (languageTranslations as Record<string, string>)[key] || (translations.en as Record<string, string>)[key] || key
  }
  